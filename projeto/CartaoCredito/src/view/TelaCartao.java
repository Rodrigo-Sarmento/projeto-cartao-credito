    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
       modelCartao = new ModelCartao();
       modelCartao.setNome(jTextFieldNomeCartao.getText());
       modelCartao.setCliente(Integer.parseInt(jTextFieldCodigoCliente.getText()));
       modelCartao.setCodSeguranca((jTextFieldCodSeguranca.getText()));
       modelCartao.setDataValidade(jFormattedTextFieldDataValidade.getText());
       modelCartao.setNumCartao(jFormattedTextFieldNumeroCartao.getText());

       if(salvarAlterar.equals("salvar")){
           try {
               if(CartaoController.inserirCartao(modelCartao)==true){
                   JOptionPane.showMessageDialog(this,"Cartão cadastrado com sucesso!","Sucesso",JOptionPane.INFORMATION_MESSAGE);
                   this.limparCampos();
                   this.DesabilitarCampos();
                   this.carregarCartao();
                   salvarAlterar="salvar";
               }else{
                   JOptionPane.showMessageDialog(this,"Erro ao cadastar cartao!","ERRO",JOptionPane.ERROR_MESSAGE);
               }  } catch (SQLException ex) {
               Logger.getLogger(TelaCargos.class.getName()).log(Level.SEVERE, null, ex);
           }
      }else{
           modelCartao.setIdCartao(Integer.parseInt(jTextFieldIdCartao.getText()));
           try {
               if(CartaoController.alterarCartao(modelCartao)==true){
                   JOptionPane.showMessageDialog(this,"Cartao alterado com sucesso!","Sucesso",JOptionPane.INFORMATION_MESSAGE);
                   this.limparCampos();
                   this.DesabilitarCampos();
                   this.carregarCartao();
                   salvarAlterar="salvar";
               }else{
                   JOptionPane.showMessageDialog(this,"Erro ao alterar cartao!","ERRO",JOptionPane.ERROR_MESSAGE);
               }
           } catch (SQLException ex) {
               Logger.getLogger(TelaCargos.class.getName()).log(Level.SEVERE, null, ex);
           }
       }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

