    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
       salvarAlterar="alterar";
       this.habilitarCampos();
       modelCartao = new ModelCartao();
       int linha = jTableCartoes.getSelectedRow();
       int codigo=0;
        if(linha<0){
            JOptionPane.showMessageDialog(this,"Selecione um cartão na tabela, antes de clicar em alterar!","Atenção",JOptionPane.WARNING_MESSAGE);
        }else{
            codigo =(int) jTableCartoes.getValueAt(linha,0);
           try {
               modelCartao = CartaoController.consultarCartao(codigo);
           } catch (SQLException ex) {
               Logger.getLogger(TelaCargos.class.getName()).log(Level.SEVERE, null, ex);
           }
            jTextFieldIdCartao.setText(String.valueOf(modelCartao.getIdCartao()));
            jTextFieldNomeCartao.setText(modelCartao.getNome());
            jFormattedTextFieldDataValidade.setText(modelCartao.getDataValidade());
            jTextFieldCodSeguranca.setText(modelCartao.getCodSeguranca());
            jFormattedTextFieldNumeroCartao.setText(modelCartao.getNumCartao());
            
            jTabbedPane1.setSelectedIndex(0);
        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

