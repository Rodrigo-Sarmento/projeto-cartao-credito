    private void carregarCartao(){
        try {
            listaModelCartao =CartaoController.listarCartao();
        } catch (SQLException ex) {
            Logger.getLogger(TelaCargos.class.getName()).log(Level.SEVERE, null, ex);
        }
        DefaultTableModel model = (DefaultTableModel) jTableCartoes.getModel();
        //número de linhas que a tabela começa
        model.setNumRows(0);
        
        //a variável cont recebe o tamanho da lista
        int cont = listaModelCartao.size();
        //inserir clientes na tabela
        for(int c =0; c<cont;c++){
            model.addRow(new Object[]{
                listaModelCartao.get(c).getIdCartao(),
                listaModelCartao.get(c).getNumCartao(),
                listaModelCartao.get(c).getNome(),
                listaModelCartao.get(c).getDataValidade(),
                listaModelCartao.get(c).getCodSeguranca(),
        
            });
        }
    }
    
