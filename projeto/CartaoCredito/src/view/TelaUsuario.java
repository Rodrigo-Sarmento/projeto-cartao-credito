    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
       modelUsuario = new ModelUsuario();
       modelUsuario.setLogin(jTextFieldUsuario.getText());
       modelUsuario.setSenha((jPasswordFieldSenha.getText()));
       modelUsuario.setFuncionario(Integer.parseInt(jTextFieldIdFuncionario.getText()));

       if(salvarAlterar.equals("salvar")){
           try {
               if(UsuarioController.inserirUsuario(modelUsuario)==true){
                   JOptionPane.showMessageDialog(this,"Usuário cadastrado com sucesso!","Sucesso",JOptionPane.INFORMATION_MESSAGE);
                   this.limparCampos();
                   this.carregarUsuario();
                   salvarAlterar="salvar";
               }else{
                   JOptionPane.showMessageDialog(this,"Erro ao cadastar usuário!","ERRO",JOptionPane.ERROR_MESSAGE);
               }  } catch (SQLException ex) {
               Logger.getLogger(TelaCargos.class.getName()).log(Level.SEVERE, null, ex);
           }
      }else{
           modelUsuario.setIdUsuario(Integer.parseInt(jTextFieldCodigo.getText()));
           try {
               if(UsuarioController.alterarUsuario(modelUsuario)==true){
                   JOptionPane.showMessageDialog(this,"Usuário alterado com sucesso!","Sucesso",JOptionPane.INFORMATION_MESSAGE);
                   this.limparCampos();
                   this.carregarUsuario();
                   salvarAlterar="salvar";
               }else{
                   JOptionPane.showMessageDialog(this,"Erro ao alterar usuário!","ERRO",JOptionPane.ERROR_MESSAGE);
               }
           } catch (SQLException ex) {
               Logger.getLogger(TelaCargos.class.getName()).log(Level.SEVERE, null, ex);
           }
       }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

