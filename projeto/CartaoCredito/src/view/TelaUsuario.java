    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
       salvarAlterar="alterar";
       modelUsuario = new ModelUsuario();
       int linha = jTableUsuarios.getSelectedRow();
       int codigo=0;
        if(linha<0){
            JOptionPane.showMessageDialog(this,"Selecione um usuário na tabela, antes de clicar em alterar!","Atenção",JOptionPane.WARNING_MESSAGE);
        }else{
            codigo =(int) jTableUsuarios.getValueAt(linha,0);
           try {
               modelUsuario = UsuarioController.consultarUsuario(codigo);
           } catch (SQLException ex) {
               Logger.getLogger(TelaCargos.class.getName()).log(Level.SEVERE, null, ex);
           }
            jTextFieldCodigo.setText(String.valueOf(modelUsuario.getIdUsuario()));
            jTextFieldUsuario.setText(modelUsuario.getLogin());
            jPasswordFieldSenha.setText(modelUsuario.getSenha());
            jTextFieldIdFuncionario.setText(modelUsuario.getFuncionario()+"");
            
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
     int linha = jTableUsuarios.getSelectedRow();
     int codigo = 0;
        if(linha<0){
            JOptionPane.showMessageDialog(this,"Selecione um usuário na tabela, antes de clicar em excluir!","Atenção",JOptionPane.WARNING_MESSAGE);
        }else{
            String nome = (String)jTableUsuarios.getValueAt(linha,1);
            int opcao= JOptionPane.showConfirmDialog(this,"Tem certeza que deseja excluir o usuário: "+nome,"Atenção",JOptionPane.YES_NO_OPTION);
            if(opcao==JOptionPane.OK_OPTION){
                codigo = (Integer) jTableUsuarios.getValueAt(linha,0);
                try {
                    UsuarioController.excluirUsuario(codigo);
                } catch (SQLException ex) {
                    Logger.getLogger(TelaCargos.class.getName()).log(Level.SEVERE, null, ex);
                }
        }
       }
        carregarUsuario();
    }//GEN-LAST:event_jButtonExcluirActionPerformed

      private void preencherCodigoFuncionarioPeloComboBox() {
        try {
            modelFuncionario = FuncionarioController.consultarFuncionarioPeloNome(uJComboBoxFuncionario.getSelectedItem().toString());
        } catch (SQLException ex) {
            Logger.getLogger(TelaCartao.class.getName()).log(Level.SEVERE, null, ex);          
        }
        jTextFieldIdFuncionario.setText(String.valueOf(modelFuncionario.getIdFuncionario()));
    }
    
    private void listarFuncionarios() {
        try {
            listaModelFuncionario = FuncionarioController.listarFuncionario();
        } catch (SQLException ex) {
            Logger.getLogger(TelaCartao.class.getName()).log(Level.SEVERE, null, ex);
        }
        uJComboBoxFuncionario.removeAllItems();
        for (int i = 0; i < listaModelFuncionario.size(); i++) {
            uJComboBoxFuncionario.addItem(listaModelFuncionario.get(i).getNome());
        }
    }
    
    private void limparCampos(){
         jTextFieldCodigo.setText("");
         jTextFieldUsuario.setText("");
         jPasswordFieldSenha.setText("");

     }
    
    private void carregarUsuario(){
        try {
            listaModelUsuario =UsuarioController.listarUsuario();
        } catch (SQLException ex) {
            Logger.getLogger(TelaCargos.class.getName()).log(Level.SEVERE, null, ex);
        }
        DefaultTableModel model = (DefaultTableModel) jTableUsuarios.getModel();
        //número de linhas que a tabela começa
        model.setNumRows(0);
        
        //a variável cont recebe o tamanho da lista
        int cont = listaModelUsuario.size();
        //inserir clientes na tabela
        for(int c =0; c<cont;c++){
            model.addRow(new Object[]{
                listaModelUsuario.get(c).getIdUsuario(),
                listaModelUsuario.get(c).getLogin(),
                listaModelUsuario.get(c).getFuncionario(),
      
        
            });
        }
    }
    
